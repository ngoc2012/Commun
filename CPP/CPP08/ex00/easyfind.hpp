#ifndef EASYFIND_HPP
# define EASYFIND_HPP

#include <iostream>
#include <algorithm>

template <typename T>
T*    easyfind(T& c, int v )
{
    T*  o = std::find(c.begin(), c.end(), v);
    if (o != c.end())
        return (o);
    else
        return (0);
}

#endif
