#include "minishell.h"

static int	abs_val(int i)
{
	if (i < 0)
		return (i + 256);
	return (i);
}

int	ft_strcmp_val(char *s1, char *s2)
{
	if (!s1 || !s2)
		return (0);
	if (*s1 == '\\')
		s1++;
	if (*s2 == '\\')
		s2++;
	while (*s1 == *s2 && *s1 && *s2)
	{
		s1++;
		s2++;
		if (*s1 == '\\')
			s1++;
		if (*s2 == '\\')
			s2++;
	}
	if (abs_val(*s1) <= abs_val(*s2))
		return (1);
	return (0);
}
