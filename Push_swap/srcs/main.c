#include "push_swap.h"

int	main(int argc, char **argv)
{
	t_stack	*st;

	if (argc < 2)
		exit(EXIT_SUCCESS);
	st = get_args(argc, argv);
	if (!st)
		exit(EXIT_FAILURE);
	calculate(st);
	st->free(st);
	exit(EXIT_SUCCESS);
}
